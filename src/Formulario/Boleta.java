    private void cboCategoriaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboCategoriaItemStateChanged
        String categoria=(String)cboCategoria.getSelectedItem();
         
         if(categoria.equals("Seleccionar")){
             txtPrecio.setText("");
         }
         if(categoria.equals("Golden")){
             txtPrecio.setText("220");
         }
         if(categoria.equals("Premium")){
             txtPrecio.setText("112");
         }
         if(categoria.equals("Vip")){
             txtPrecio.setText("65");
         }
         if(categoria.equals("General")){
             txtPrecio.setText("40");
         }
         
    }//GEN-LAST:event_cboCategoriaItemStateChanged

