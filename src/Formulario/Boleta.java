    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
       
     if(txtCantidad.getText().isEmpty()|| cboCategoria.getSelectedIndex()==0 || cboTipo.getSelectedIndex()==0 ){
                JOptionPane.showMessageDialog(this, "No dejar campos vacios");
        }else{
                int cantidad=Integer.parseInt(txtCantidad.getText());
                double  precio=Double.parseDouble(txtPrecio.getText());
                double  subtotal=(cantidad*precio);

       
                //para llenar la tabla
                String pro=cboCategoria.getSelectedItem().toString();
                String tipo=cboTipo.getSelectedItem().toString();
                String pre= ""+ precio;
                String can= "" + cantidad;
                String sub= "" + subtotal;

                 String datos[]={pro,pre,can,tipo,sub};
                 modelo.addRow(datos);

               //---------------------------------------------------------------------------------  
                 txtPrecio.setText("");
                 txtCantidad.setText("");
               //---------------------------------------------------------------------------------  
                contador++;
                String numero =String.valueOf(contador);
                lblNumero.setText(numero);

                //se agrega un acumulador para el total de ventas
                acumulador=(int) (acumulador+subtotal);
                //declaro valor para almacenar
                String numero2 =String.valueOf(acumulador);
                //mostrar valor almacenado
                lblTotal.setText(numero2);
           }
    }//GEN-LAST:event_btnCalcularActionPerformed

